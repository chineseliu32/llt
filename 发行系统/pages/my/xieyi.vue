	      if (this.seconds === 0) {
	        // 停止计时器
	        clearInterval(this.timer);
	        // 跳转到登录页面
	        this.$router.push('/login');
	      } else {
	        // 启动计时器
	        this.timer = setInterval(() => {
	          if (this.seconds > 0) {
	            this.seconds--;
	          } else {
	            clearInterval(this.timer);
	          }
	        }, 1000);
	      }
	    },
		gotoLogin()
		{
			uni.setStorageSync("yinsi","true");
